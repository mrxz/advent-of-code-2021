let points = {
    ')': 3,
    ']': 57,
    '}': 1197,
    '>': 25137,
}

let invert  = {
    '(': ')',
    '[': ']',
    '{': '}',
    '<': '>',
}

let sum = 0;
let incomplete_lines = input.map(line => {
    let stack = [];
    let chars = [...line];
    for(let i = 0; i < chars.length; i++) {
        let c = chars[i];
        if('{[(<'.indexOf(c) !== -1) {
            stack.push(c);
        } else {
            if(invert[stack.pop()] != c) {
                sum += points[c];
                return false;
            }
        }
    }
    return stack;
}).filter(x => x !== false);

console.log(sum);

